public class Squad {
    private String mName;
    private String mSize;
    private  String mCause;
    public Squad(String name, String size, String cause) {
        mName= name;
        mSize= size;
        mCause= cause;
    }
    public String getmName() {
        return mName;
    }

    public String getmSize() {
        return mSize;
    }

    public String getmCause() {
        return mCause;
    }
}
